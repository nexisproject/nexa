package shrmdq

import (
	"testing"

	"github.com/stretchr/testify/require"
)

func TestMontage(t *testing.T) {
	b := []byte{0x11, 0x00, 0x4D, 0x4C, 0x33, 0x30, 0x37, 0x41, 0x5F, 0x76, 0x32, 0x30, 0x32, 0x33, 0x30, 0x34, 0x32, 0x32, 0x52, 0x0F, 0x00, 0x38, 0x36, 0x34, 0x36, 0x30, 0x36, 0x30, 0x36, 0x30, 0x31, 0x32, 0x36, 0x33, 0x34, 0x38, 0x14, 0x00, 0x38, 0x39, 0x38, 0x36, 0x30, 0x36, 0x32, 0x30, 0x32, 0x33, 0x30, 0x30, 0x33, 0x37, 0x30, 0x37, 0x38, 0x37, 0x34, 0x32, 0x01, 0x00, 0x1F, 0x2A, 0x00, 0xFE, 0xFE, 0xFE, 0xFE, 0x68, 0x77, 0x32, 0x09, 0x19, 0x04, 0x22, 0x68, 0x91, 0x1A, 0x34, 0x37, 0x33, 0x37, 0xAA, 0x65, 0x3C, 0x4C, 0x37, 0x55, 0x65, 0x63, 0x65, 0x65, 0x63, 0x68, 0x63, 0x67, 0x92, 0xA9, 0x9A, 0x6C, 0x6B, 0x64, 0x64, 0x33, 0x32, 0x16, 0x00, 0x00, 0x04, 0x00, 0x01, 0x00, 0x00, 0x00}
	mo, err := NewMontage(b).Decompose()
	require.NoError(t, err)
	want := [][]byte{
		{0x4D, 0x4C, 0x33, 0x30, 0x37, 0x41, 0x5F, 0x76, 0x32, 0x30, 0x32, 0x33, 0x30, 0x34, 0x32, 0x32, 0x52},
		{0x38, 0x36, 0x34, 0x36, 0x30, 0x36, 0x30, 0x36, 0x30, 0x31, 0x32, 0x36, 0x33, 0x34, 0x38},
		{0x38, 0x39, 0x38, 0x36, 0x30, 0x36, 0x32, 0x30, 0x32, 0x33, 0x30, 0x30, 0x33, 0x37, 0x30, 0x37, 0x38, 0x37, 0x34, 0x32},
		{0x1f},
		{0xFE, 0xFE, 0xFE, 0xFE, 0x68, 0x77, 0x32, 0x09, 0x19, 0x04, 0x22, 0x68, 0x91, 0x1A, 0x34, 0x37, 0x33, 0x37, 0xAA, 0x65, 0x3C, 0x4C, 0x37, 0x55, 0x65, 0x63, 0x65, 0x65, 0x63, 0x68, 0x63, 0x67, 0x92, 0xA9, 0x9A, 0x6C, 0x6B, 0x64, 0x64, 0x33, 0x32, 0x16},
	}
	got := mo.GetDecomposed()
	require.Equal(t, want, got)

	require.Equal(t, [][]byte{{0x00, 0x00, 0x04, 0x00, 0x01, 0x00, 0x00, 0x00}}, mo.GetUnread())
	require.Equal(t, b, mo.Compose().GetComposed())
}
